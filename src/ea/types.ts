// ─── Status & Confidence ────────────────────────────────────────────────────────

/**
 * Lifecycle status of an artifact.
 *
 * EA-native lifecycle: draft → active → deprecated → retired
 * Legacy-compat lifecycle: draft → planned → active → shipped → deprecated → retired
 */
export type ArtifactStatus =
  | "draft"
  | "planned"
  | "active"
  | "shipped"
  | "deprecated"
  | "retired"
  | "deferred";

/**
 * How this artifact's information was established.
 *
 * - `declared` — Human-authored, reviewed, intentional (highest trust)
 * - `observed` — Derived from an authoritative external source (medium trust)
 * - `inferred` — Generated by discovery heuristics, needs review (lowest trust)
 */
export type ArtifactConfidence = "declared" | "observed" | "inferred";

// ─── Anchors ────────────────────────────────────────────────────────────────────

/**
 * Anchor points binding an artifact to observable code, APIs, schemas,
 * infrastructure, and other verifiable locations. Resolvers use anchors
 * to detect drift between declared and observed state.
 *
 * Replaces and extends the existing `SemanticRefs` model.
 */
export interface EaAnchors {
  /** Code symbols: class names, function names, exported constants */
  symbols?: string[];

  /** API endpoints: "POST /orders", "GET /orders/:id" */
  apis?: string[];

  /** Event topics/types: "order.created", "payment.processed" */
  events?: string[];

  /** Schema references: "OrderSchema", "orders.order" */
  schemas?: string[];

  /** Infrastructure references: "terraform:module.order_service", "kubernetes:deployment/order-service" */
  infra?: string[];

  /** Catalog references: "service:order-service", "dataset:orders" */
  catalogRefs?: string[];

  /** IAM references: "aws:iam-role/order-service" */
  iam?: string[];

  /** Network references: "aws:security-group/sg-12345" */
  network?: string[];

  /** Status/state enum values: "open", "closed", "pending" */
  statuses?: string[];

  /** State machine transitions: "open→processing", "processing→completed" */
  transitions?: string[];

  /** Extensible anchors for tools not covered above */
  other?: Record<string, string[]>;
}

// ─── Relations ──────────────────────────────────────────────────────────────────

/**
 * A directional typed link between two artifacts.
 * Only canonical (forward) directions are stored; inverses are computed
 * by the graph builder.
 */
export interface EaRelation {
  /** Relation type (canonical direction only). */
  type: string;

  /** Target artifact ID. */
  target: string;

  /** Human-readable description of this specific relation instance. */
  description?: string;

  /** How critical this relation is. */
  criticality?: "low" | "medium" | "high" | "critical";

  /** Whether this relation is still active. */
  status?: "active" | "deprecated";
}

// ─── Trace Refs ─────────────────────────────────────────────────────────────────

/** A trace link to an external document, URL, or artifact. */
export interface EaTraceRef {
  /** Path to external document, URL, or artifact ID. */
  path: string;

  /** Role of this trace link. */
  role?:
    | "specification"
    | "evidence"
    | "rationale"
    | "context"
    | "implementation"
    | "test";

  /** Human-readable label. */
  label?: string;
}

