// ─── ID Validation ──────────────────────────────────────────────────────────────

/**
 * Validates an EA artifact ID against the format: `{domain}/{PREFIX}-{slug}`
 * or the short form `{PREFIX}-{slug}`.
 *
 * @deprecated Legacy ID format. Backstage entities use entity references instead.
 */
export function isValidEaId(id: string, kind?: string): boolean {
  // Allow domain-qualified or short IDs
  const parts = id.includes("/") ? id.split("/") : [undefined, id];
  if (parts.length > 2) return false;

  const [domain, localId] = parts;

  // If domain provided, validate it
  if (domain && !EA_DOMAINS.includes(domain as EaDomain)) return false;

  // LocalId must be PREFIX-slug
  if (!localId || !localId.includes("-")) return false;

  // Extract prefix (everything before the first dash in slug portion)
  const dashIndex = localId.indexOf("-");
  const prefix = localId.substring(0, dashIndex);
  const slug = localId.substring(dashIndex + 1);

  if (!prefix || !slug) return false;

  // Slug must be kebab-case
  if (!/^[a-z][a-z0-9]*(-[a-z0-9]+)*$/.test(slug)) return false;

  // If kind is provided, validate prefix matches
  if (kind) {
    const expected = getKindPrefix(kind);
    if (expected && expected !== prefix) return false;
  }

  return true;
}
