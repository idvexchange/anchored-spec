// ─── Artifact Base Shape ────────────────────────────────────────────────────────

/**
 * The unified base shape shared by all EA artifacts.
 * Every kind extends this with additional kind-specific fields.
 *
 * Design reference: docs/ea-unified-artifact-model.md §Unified Base Shape
 *
 * @deprecated Use `BackstageEntity` from `./backstage/types.js` instead.
 * This type is retained for backward compatibility during migration.
 * Use the accessor functions from `./backstage/accessors.js` to read fields
 * from BackstageEntity in a way that parallels EaArtifactBase field access.
 */
export interface EaArtifactBase {
  /** Artifact identifier: `{domain}/{KIND_PREFIX}-{slug}` */
  id: string;

  /** Schema version of this artifact's content (team-managed). */
  schemaVersion: string;

  /** The kind discriminator — determines which extended fields apply. */
  kind: string;

  /** Human-readable title. */
  title: string;

  /** Lifecycle status. */
  status: ArtifactStatus;

  /** Human-readable summary of what this artifact represents. */
  summary: string;

  /** Team or individual owners. */
  owners: string[];

  /** How this artifact's information was established. */
  confidence: ArtifactConfidence;

  /** Freeform tags for filtering and grouping. */
  tags?: string[];

  /** Typed directional relations to other artifacts. */
  relations?: EaRelation[];

  /** Anchor points to observable code/infra/APIs. */
  anchors?: EaAnchors;

  /** Trace links to external documents, evidence, rationale. */
  traceRefs?: EaTraceRef[];

  /** Risk assessment metadata. */
  risk?: EaRiskAssessment;

  /** Compliance metadata. */
  compliance?: EaComplianceMetadata;

  /** Extensible metadata for plugins and custom tooling. */
  extensions?: Record<string, unknown>;
}

