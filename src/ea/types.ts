/**
 * Anchored Spec — Enterprise Architecture Types
 *
 * Core type definitions for the EA extension. All EA artifacts share a
 * common base shape (`EaArtifactBase`) and extend it with kind-specific fields.
 *
 * Design reference: docs/ea-unified-artifact-model.md
 */

// ─── Domains ────────────────────────────────────────────────────────────────────

/** The six EA domains, each grouping related artifact kinds. */
